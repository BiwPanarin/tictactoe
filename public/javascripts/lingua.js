function choosenAppendict(language, type, key) {
    return appendict[language][type][key]
}

function choosenLang(language, Path, key) {
    return languages[language][Path][key]
}

function changeSelectOptionAppendict(language) {
    for (let key in appendict[language]) {
        if (document.querySelector('[appendict-type]') && document.querySelector('[appendict]')) {
            let type, set;
            for (let n = 0; n < $(document).find('[appendict-type]').length; n++) {
                type = document.querySelectorAll('[appendict-type]')[n].attributes['appendict-type'].nodeValue
                set = document.querySelectorAll('[appendict]')[n].attributes['appendict'].nodeValue
                $(document).find('[appendict-type]')[n].textContent = appendict[language][type][set]
                $(document).find('[appendict-type]')[n].textContent = appendict[language][type][set]
            }
        }
    }
}

function changeAppendict(language) {
    for (let key in appendict[language]) {
        if (document.querySelector('[appendict-type]') && document.querySelector('[appendict]')) {
            let type, set;
            const oppositeLang = language == 'EN' ? 'TH' : 'EN';
            for (let n = 0; n < $(document).find('[appendict-type]').length; n++) {
                type = document.querySelectorAll('[appendict-type]')[n].attributes['appendict-type'].nodeValue
                set = document.querySelectorAll('[appendict]')[n].attributes['appendict'].nodeValue
                $(document).find('[appendict-type]')[n].textContent = appendict[language][type][set]

                if (document.querySelectorAll('[appendict]')[n].attributes['data-subtext']) {
                    document.querySelectorAll('[appendict]')[n].attributes['data-subtext'].nodeValue = appendict[oppositeLang][type][set]
                }
            }
        }
    }
}

function changeAppendictDatatable(language) {

    for (let key in appendict[language]["datatable"]) {

        const keyElement = document.querySelectorAll(`.${key}`)

        if (keyElement != undefined && keyElement != null) {
            for (let n = 0; n < keyElement.length; n++) {
                
                if (key == "dataTables_info") {
                    const numOnly = keyElement[n].textContent.match(/\d+/g)
                    const _START_ = numOnly[0]
                    const _END_ = numOnly[1]
                    const _TOTAL_ = numOnly[2]
                    keyElement[n].textContent  = appendict[language]["datatable"][key].replace(/_START_/g, _START_).replace(/_END_/g, _END_).replace(/_TOTAL_/g, _TOTAL_)
                }
                else {
                    keyElement[n].textContent  = appendict[language]["datatable"][key]
                }
            }
        }
    }

}

function changeLanguage(language, Path) {

    if (Path != "login") {
        for (let util in languages[language].utility) {

            const utilElement = document.querySelectorAll(`.${util}`)

            if (utilElement != undefined && utilElement != null) {
                for (let n = 0; n < utilElement.length; n++) {
                    utilElement[n].textContent  = languages[language].utility[util]
                }
            }
        }
    }

    for (let key in languages[language][Path]) {

        const keyElement = document.querySelectorAll(`.${key}`)

        if (keyElement != undefined && keyElement != null) {
            for (let n = 0; n < keyElement.length; n++) {
                if (keyElement[n].type == 'button') {
                    keyElement[n].value  = languages[language][Path][key]
                }
                else if (keyElement[n].type == 'text' || keyElement[n].type == 'password' || keyElement[n].type == 'textarea') {
                    keyElement[n].placeholder  = languages[language][Path][key]
                }
                else if (keyElement[n].type == undefined || keyElement[n].type == "") {
                    keyElement[n].textContent  = languages[language][Path][key]
                }
            }
        }

    }

return 'success';
}